'''
ex12 - Tendo como dados de entrada a altura de uma pessoa, construa um algoritmo que calcule seu peso ideal

fórmula: (72.7*altura) - 58
'''

altura = float(input('\nPara calcular seu peso ideal, digite sua altura: '))

pesoIdeal = (72.7*altura) - 58

print(f'Seu peso ideal é de: {pesoIdeal}\n')
